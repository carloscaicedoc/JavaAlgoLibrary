
public class Kata {
    // Ternary Operator solution
    public static String boolToWordTernaryOperator(boolean b) {
        return b ? "YES" : "NO";
    }

    // Simple conditional statement approach
    public static String boolToWord(boolean b) {
        if (b) {
            return "Yes";
        } else {
            return "No";
        }
    }
}